    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String sid_raw = request.getParameter("sid");
        QuestionDAO dao = new QuestionDAO();
        SessionDAO sDao = new SessionDAO();
        int sid;
        try {
            sid = Integer.parseInt(sid_raw);
            request.setAttribute("sid", sid_raw);
            request.setAttribute("qlist", dao.getQuestionbySessionID(sid));
            request.setAttribute("slist", sDao.getAllSession());
        } catch (Exception e) {
            System.out.println(e);
        }
        request.getRequestDispatcher("designer/question/showquestion.jsp").forward(request, response);

    }

